public class DortIslem {
    public int sumOfNumbers(int number1 , int number2) {
        return number1+number2;
    }
    public int subOfNumbers(int number1 , int number2) {
        return number1-number2;
    }
    public int multipleOfNumbers(int number1 , int number2) {
        return number1*number2;
    }
    public int divideOfNumbers(int number1 , int number2) {
        return number1/number2;
    }
}
